package net.continuumsecurity.restyburp.server;

import javax.ws.rs.WebApplicationException;
import javax.ws.rs.core.Response;

/**
 *
 * @author stephen
 */
public class ServerErrorException extends WebApplicationException {

    public ServerErrorException(Response.Status responseCode,String message) {
        super(Response.status(responseCode).entity(new ErrorBean(responseCode.getStatusCode(),message)).build());
    }
}
